const canvas = document.getElementById("matrix");
const ctx = canvas.getContext("2d");

function resizeCanvas() {
    const header = document.querySelector("header");

    canvas.width = header.clientWidth;
    canvas.height = header.clientHeight;
}

resizeCanvas();

const letters = "01{}[]<>$#/";
const fontSize = 16;

const columns = Math.floor(canvas.width / fontSize);

const drops = Array(columns).fill(1);

function drawMatrix() {
    ctx.fillStyle = "rgba(7,11,16,0.08)";
    ctx.fillRect(0, 0, canvas.width, canvas.height);

    ctx.fillStyle = "#00ff9c";
    ctx.font = fontSize + "px monospace";

    for (let i = 0; i < drops.length; i++) {
        const text =
            letters[Math.floor(Math.random() * letters.length)];

        ctx.fillText(text, i * fontSize, drops[i] * fontSize);

        if (
            drops[i] * fontSize > canvas.height &&
            Math.random() > 0.975
        ) {
            drops[i] = 0;
        }

        drops[i]++;
    }
}

setInterval(drawMatrix, 50);

window.addEventListener("resize", resizeCanvas);

const konamiCode = [
  "ArrowUp", "ArrowUp",
  "ArrowDown", "ArrowDown",
  "ArrowLeft", "ArrowRight",
  "ArrowLeft", "ArrowRight",
  "b", "a"
];

let konamiIndex = 0;

document.addEventListener("keydown", function (event) {
  const key = event.key.toLowerCase();

  if (key === konamiCode[konamiIndex].toLowerCase()) {
    konamiIndex++;

    if (konamiIndex === konamiCode.length) {
      spawnSeal();
      konamiIndex = 0;
    }
  } else {
    konamiIndex = 0;
  }
});

function spawnSeal() {
  const seal = document.createElement("div");

  seal.textContent = "🦭";
  seal.style.position = "fixed";
  seal.style.left = "-100px";
  seal.style.top = Math.random() * (window.innerHeight - 100) + "px";
  seal.style.fontSize = "80px";
  seal.style.zIndex = "999999";
  seal.style.pointerEvents = "none";
  seal.style.transition = "left 6s linear";

  document.body.appendChild(seal);

  requestAnimationFrame(() => {
    seal.style.left = window.innerWidth + 100 + "px";
  });

  setTimeout(() => {
    seal.remove();
  }, 6000);
}

